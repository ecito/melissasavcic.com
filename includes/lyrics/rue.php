<p><strong>Verse I</strong></p>
<p>Je déjeune seule au café de la mairie<br />
J'observe les personnes passer et me met à rêver<br />
La jupe froissée, vêtement d'hier, le pas pressé<br />
léger sourire qu'elle porte</p>
<p>Un homme courbé, tout en silence, la suit de près<br />
Est-il celui, qu'elle a quitté, avec regret<br />
Je n'y crois pas, elle rêve encore, à ses caresses<br />
Lui est perdu dans son ennui</p>
